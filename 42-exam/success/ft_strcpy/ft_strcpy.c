#include <unistd.h>

char	*ft_strcpy(char *s1, char *s2)
{
	char	*org;

	org = s1;
	while (*s2 != '\0')
	{
		*s1 ++ = *s2 ++;
	}
	*s1 = '\0';
	return (org);
}
